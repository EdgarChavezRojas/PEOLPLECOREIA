CREATE TABLE clocking_device (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  version BIGINT,
  tenant_id UUID NOT NULL,
  created_at TIMESTAMP NOT NULL,
  created_by VARCHAR(255),
  last_modified_at TIMESTAMP,
  last_modified_by VARCHAR(255),
  device_id UUID NOT NULL,
  org_unit_id UUID NOT NULL,
  serial_number VARCHAR(100) NOT NULL,
  device_type VARCHAR(30) NOT NULL,
  device_role VARCHAR(20) NOT NULL,
  status VARCHAR(20) NOT NULL,
  installed_at TIMESTAMP,
  decommissioned_at TIMESTAMP,
  supports_fingerprint BOOLEAN NOT NULL,
  supports_facial BOOLEAN NOT NULL,
  supports_nfc BOOLEAN NOT NULL,
  supports_qr BOOLEAN NOT NULL,
  firmware_version VARCHAR(50),
  public_key_pem TEXT,
  last_seen_at TIMESTAMP,
  battery_level INTEGER,
  sync_status VARCHAR(20),
  enrolled_employees_count INTEGER,
  CONSTRAINT uk_device_serial_tenant UNIQUE (serial_number, tenant_id)
);

